bool contains_kanji(const std::string& utf8str) {
    std::wstring_convert<std::codecvt_utf8<char32_t>, char32_t> converter;
    std::u32string utf32 = converter.from_bytes(utf8str);

    for (char32_t ch : utf32) {
        if ((ch >= 0x4E00 && ch <= 0x9FFF) ||  // CJK Unified Ideographs
            (ch >= 0x3400 && ch <= 0x4DBF)) {  // Extension A
            return true;
        }
    }
    return false;
}

bool contains_japanese_characters(const std::string& utf8str) {
    std::wstring_convert<std::codecvt_utf8<char32_t>, char32_t> converter;
    std::u32string utf32 = converter.from_bytes(utf8str);

    for (char32_t ch : utf32) {
        // CJK Unified Ideographs (Kanji)
        if ((ch >= 0x4E00 && ch <= 0x9FFF) ||
            // CJK Unified Ideographs Extension A
            (ch >= 0x3400 && ch <= 0x4DBF) ||
            // Full-width Katakana
            (ch >= 0x30A0 && ch <= 0x30FF) ||
            // Half-width Katakana
            (ch >= 0xFF65 && ch <= 0xFF9F)) {
            return true;
        }
    }
    return false;
}

void skipSpaces(const std::string& formula, size_t& i) {
    while (i < formula.size() && isspace(formula[i])) i++;
}

double parseNumber(const std::string& formula, size_t& i) {
    skipSpaces(formula, i);
    const char* start = formula.c_str() + i;
    char* end = NULL;
    double num = strtod(start, &end);
    size_t newPos = end - formula.c_str();
    if (newPos == i) return 1.0;
    i = newPos;
    return num;
}

std::pair<std::string, double> parseElement(const std::string& formula, size_t& i) {
    skipSpaces(formula, i);
    std::string element;
    if (isupper(formula[i])) {
        element += formula[i++];
        if (i < formula.size() && islower(formula[i])) element += formula[i++];
    }
    double count = parseNumber(formula, i);
    return make_pair(element, count);
}


std::map<std::string, double> parseFormula(const std::string& formula, size_t& i) {
    std::map<std::string, double> elementCount;
    while (i < formula.size()) {
        skipSpaces(formula, i);
        if (i >= formula.size()) break;
        if (formula[i] == '(' || formula[i] == '[') {
            char open = formula[i++];
            char close = (open == '(') ? ')' : ']';
            std::map<std::string, double> inner = parseFormula(formula, i);
            skipSpaces(formula, i);
            if (i >= formula.size() || formula[i] != close) {
                writelog << "Error: Mismatched parentheses at position " << i << std::endl;
                writelog.flush();
                failure = true;
            }
            if (!failure) {
                i++;
                double multiplier = parseNumber(formula, i);
                for (auto& kv : inner) elementCount[kv.first] += kv.second * multiplier;
            }
        }
        else if (formula[i] == ')' || formula[i] == ']') {
            return elementCount;
        }
        else if (isupper(formula[i])) {
            std::pair<std::string, double> p = parseElement(formula, i);
            elementCount[p.first] += p.second;
        }
        else if (isdigit(formula[i])) {
            double multiplier = parseNumber(formula, i);
            elementCount = parseFormula(formula, i);
            skipSpaces(formula, i);
            for (auto& kv : elementCount) kv.second *= multiplier;
        }
        else {
            break;
        }

        if (failure) {
            break;
        }

    }
    return elementCount;
}


double computeFormulaMass(const std::map<std::string, double>& formula) {
    double mass = 0.0;
    for (auto& kv : formula) {
        auto it = atomicMass.find(kv.first);
        if (it == atomicMass.end()) {
            writelog << "Error: Atomic mass of " << kv.first << " not found!" << std::endl;
            writelog.flush();
            failure = true;
        }
        if (!failure) {
            mass += it->second * kv.second;
        }
        else {
            break;
        }
    }
    return mass;
}

Part parsePart(const std::string& input) {
    
    size_t molPos = input.find("mol%");
    size_t atPos = input.find("at%");
    size_t nPos = input.find("%");

    size_t wtPos = input.find("wt%");
    size_t wtdPos = input.find("wt.%");
    size_t massPos = input.find("mass%");
    
    Part part;

    if (molPos != std::string::npos) {
        std::string number = input.substr(0, molPos);
        part.multiplier = strtod(number.c_str(), NULL) / 100.0;
        part.type = "mol";
        part.formula = input.substr(molPos + 4);
    }
    else if (atPos != std::string::npos) {
        std::string number = input.substr(0, atPos);
        part.multiplier = strtod(number.c_str(), NULL) / 100.0;
        part.type = "mol";
        part.formula = input.substr(atPos + 3);
    }
    else if (nPos != std::string::npos && wtPos == std::string::npos && massPos == std::string::npos) {
        std::string number = input.substr(0, nPos);
        part.multiplier = strtod(number.c_str(), NULL) / 100.0;
        part.type = "mol";
        part.formula = input.substr(nPos + 1);
    }
    else if (wtPos != std::string::npos) {
        std::string number = input.substr(0, wtPos);
        part.multiplier = strtod(number.c_str(), NULL) / 100.0;
        part.type = "wt";
        part.formula = input.substr(wtPos + 3);
    }
    else if (wtdPos != std::string::npos) {
        std::string number = input.substr(0, wtdPos);
        part.multiplier = strtod(number.c_str(), NULL) / 100.0;
        part.type = "wt";
        part.formula = input.substr(wtdPos + 4);
    }
    else if (massPos != std::string::npos) {
        std::string number = input.substr(0, massPos);
        part.multiplier = strtod(number.c_str(), NULL) / 100.0;
        part.type = "wt";
        part.formula = input.substr(massPos + 5);
    }
    else {
        part.multiplier = 1.0;
        part.type = "mol"; // Base formulas are treated as mol by default
        part.formula = input;
    }
    while (!part.formula.empty() && isspace(part.formula[0])) part.formula.erase(0, 1);
    return part;
}

void engine(std::string input) {

    if (input.find("-") == std::string::npos && input.find("vol") == std::string::npos && input.find("x") == std::string::npos && input.find("null") == std::string::npos && input.find("NULL") == std::string::npos && input.find("Null") == std::string::npos) {
        
        size_t pos = 0, next = 0;
        std::map<std::string, double> totalCounts;
        std::vector<std::pair<Part, std::map<std::string, double>>> parts;

        while ((next = input.find('+', pos)) != std::string::npos) {
            std::string segment = input.substr(pos, next - pos);
            pos = next + 1;
            while (!segment.empty() && isspace(segment[0])) segment.erase(0, 1);
            while (!segment.empty() && isspace(segment[segment.size() - 1])) segment.pop_back();
            Part p = parsePart(segment);
            size_t idx = 0;
            std::map<std::string, double> parsed = parseFormula(p.formula, idx);
            parts.push_back(make_pair(p, parsed));
        }

        std::string lastPart = input.substr(pos);
        while (!lastPart.empty() && isspace(lastPart[0])) lastPart.erase(0, 1);
        while (!lastPart.empty() && isspace(lastPart[lastPart.size() - 1])) lastPart.pop_back();
        Part p = parsePart(lastPart);
        size_t idx = 0;
        std::map<std::string, double> parsed = parseFormula(p.formula, idx);
        parts.push_back(make_pair(p, parsed));

        double baseMass = 0.0;
        for (size_t i = 0; i < parts.size(); ++i) {
            if (parts[i].first.type == "mol" && parts[i].first.multiplier == 1.0) {
                baseMass += computeFormulaMass(parts[i].second);
            }
        }

        if (baseMass == 0.0) {
            for (size_t i = 0; i < parts.size(); ++i) {
                if (parts[i].first.type == "wt") {
                    baseMass += parts[i].first.multiplier * computeFormulaMass(parts[i].second);
                }
            }
        }

        writelog << "\n--- Calculation Breakdown ---\n";
        writelog.flush();

        for (size_t i = 0; i < parts.size(); ++i) {
            Part& part = parts[i].first;
            std::map<std::string, double>& formula = parts[i].second;
            double actualMoles = 0.0;
            if (part.type == "wt") actualMoles = baseMass * part.multiplier / computeFormulaMass(formula);
            else if (part.type == "mol") actualMoles = part.multiplier;
            writelog << "Component " << (i + 1) << ": " << part.formula << "\n";
            writelog.flush();
            writelog << " Type: " << part.type << "\n";
            writelog.flush();
            writelog << " Multiplier: " << part.multiplier << "\n";
            writelog.flush();
            writelog << " Moles Added: " << actualMoles << "\n";
            writelog.flush();
            if (actualMoles > 10000000000.0 || actualMoles < -10000000000.0) {
                writelog << "something wrong...\n";
                writelog.flush();
                failure = true;
            }
            for (auto& kv : formula) totalCounts[kv.first] += kv.second * actualMoles;
        }

        writelog << "\n--- Final Composition (moles) ---\n";
        writelog.flush();

        std::vector<int> whichblock;
        std::vector<int> countinsideblock;
        for (int i = 0; i < (signed)atomblocks.size(); i++) {
            countinsideblock.push_back(0);
        }

        for (auto& kv : totalCounts) {
            writelog << kv.first << ": " << kv.second << std::endl;
            writelog.flush();
            int foundblock = -1;
            for (int i = 0; i < (signed)atomblocks.size(); i++) {
                for (int j = 0; j < (signed)atomblocks[i].A.size(); j++) {
                    if (atomblocks[i].A[j] == kv.first) {
                        foundblock = i;
                        countinsideblock[foundblock]++;
                        break;
                    }
                }
                if (foundblock != -1) {
                    break;
                }
            }
            if (foundblock == -1) {
                foundblock = inoneblock;
                countinsideblock[foundblock]++;
            }
            whichblock.push_back(foundblock);
        }

        if (!failure) {
            bool last_exists = false;
            double last_number = 0.0;
            double total_weight = 0.0;
            double total_content = 0.0;
            for (int i = 0; i < (signed)atomblocks.size(); i++) {
                int j = 0;
                for (auto& kv : totalCounts) {
                    if (whichblock[j] == i) {
                        writeoutput << kv.first << "\t" << kv.second << "\t";
                        writeoutput.flush();
                        long double weight_piece;
                        for (int iatom = 0; iatom < (signed)atomcollect.size(); iatom++) {
                            if (atomcollect[iatom].A == kv.first) {
                                weight_piece = atomcollect[iatom].atommass;
                                break;
                            }
                        }
                        if (i != inocountblock) {
                            total_weight += kv.second * weight_piece;
                            total_content += kv.second;
                        }
                    }
                    j++;
                }
                writeoutput << "||\t";
                writeoutput.flush();
            }
            writeoutput << total_weight << "\t||\t" << total_content << "\t||\t" << total_weight / total_content << "\t";
            writeoutput.flush();
            for (int i = 0; i < (signed)atomblocks.size(); i++) {
                if (countinsideblock[i] > atomblocks[i].max_count) {
                    atomblocks[i].max_count = countinsideblock[i];
                }
            }
        }
        else {
            writeoutput << "failed\t";
            writeoutput.flush();
        }

        std::vector<int>().swap(whichblock);
        std::vector<int>().swap(countinsideblock);
        
    }
    else {
        writelog << "NULL\n";
        writelog.flush();
        writeoutput << "failed\t";
        writeoutput.flush();
    }


    writeoutput << "||\n";
    writeoutput.flush();

}

